    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // add button 
         try {
        String stdNo = jTextField2.getText().trim();
        String name = jTextField3.getText().trim();
        String surname = jTextField4.getText().trim();
        char gender = jTextField5.getText().trim().isEmpty() ? ' ' : jTextField5.getText().trim().charAt(0);
        String nationality = jTextField6.getText().trim();
        String dateInput = jFormattedTextField1.getText().trim();
        java.sql.Date sqlDate = java.sql.Date.valueOf(dateInput);

        
        if (stdNo.isEmpty() || name.isEmpty() || surname.isEmpty() || nationality.isEmpty() || gender == ' ' || dateInput.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

       
        try {
            sqlDate = java.sql.Date.valueOf(dateInput); 
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Invalid date format! Please use yyyy-MM-dd.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

       
        Student student = new Student(stdNo, name, surname, gender, nationality, sqlDate);
        StudentCRUD.insertStudent(student);

        JOptionPane.showMessageDialog(this, "Student added successfully!");

       
        clearFields();
        loadStudentsData();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "An error occurred while adding the student.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

