    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // update button
         
        try {
        int id = Integer.parseInt(jTableStudents.getValueAt(jTableStudents.getSelectedRow(), 0).toString());
        String stdNo = jTextField2.getText().trim();
        String name = jTextField3.getText().trim();
        String surname = jTextField4.getText().trim();
        char gender = jTextField5.getText().trim().isEmpty() ? ' ' : jTextField5.getText().trim().charAt(0);
        String nationality = jTextField6.getText().trim();
        String dateInput = jFormattedTextField1.getText().trim();

      
        if (stdNo.isEmpty() || name.isEmpty() || surname.isEmpty() || nationality.isEmpty() || dateInput.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

       
        java.sql.Date sqlDate;
        try {
            sqlDate = java.sql.Date.valueOf(dateInput);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Invalid date format! Please use yyyy-MM-dd.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Öğrenci güncelle
        Student student = new Student(stdNo, name, surname, gender, nationality, sqlDate);
        student.setId(id); 
        StudentCRUD.editStudent(student);

        JOptionPane.showMessageDialog(this, "Student updated successfully!");

        
        loadStudentsData();
    } catch (ArrayIndexOutOfBoundsException e) {
        JOptionPane.showMessageDialog(this, "Please select a student to update.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "An error occurred while updating the student.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // delete button
        try {
        int id = Integer.parseInt(jTableStudents.getValueAt(jTableStudents.getSelectedRow(), 0).toString());

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?");
        if (confirm == JOptionPane.YES_OPTION) {
            StudentCRUD.deleteStudent(id);
            JOptionPane.showMessageDialog(this, "Student deleted successfully!");

            // Tabloyu güncelle
            loadStudentsData();
        }
    } catch (ArrayIndexOutOfBoundsException e) {
        JOptionPane.showMessageDialog(this, "Please select a student to delete.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "An error occurred while deleting the student.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton3ActionPerformed

