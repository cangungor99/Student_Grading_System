    private void loadStudentsData() {
    DefaultTableModel model = (DefaultTableModel) jTableStudents.getModel();
    model.setRowCount(0); // Tablodaki mevcut verileri temizle

    List<Student> students = StudentCRUD.getAllStudents(); // Tüm öğrencileri getir
    for (Student student : students) {
        model.addRow(new Object[]{
            student.getId(),
            student.getStdNo(),
            student.getName(),
            student.getSurname(),
            student.getGender(),
            student.getNationality(),
            student.getBirthday()
        });
    }
}

